package demo.springjob.com.model;

public class Moneycard {
    private Integer id;

    private String name;

    private String phone;

    private Integer rechargeAmount;

    private String cardName;

    private Integer cardSum;

    private Integer cardBalance;

    private Integer cardUse;

    private Integer discount;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public Integer getRechargeAmount() {
        return rechargeAmount;
    }

    public void setRechargeAmount(Integer rechargeAmount) {
        this.rechargeAmount = rechargeAmount;
    }

    public String getCardName() {
        return cardName;
    }

    public void setCardName(String cardName) {
        this.cardName = cardName;
    }

    public Integer getCardSum() {
        return cardSum;
    }

    public void setCardSum(Integer cardSum) {
        this.cardSum = cardSum;
    }

    public Integer getCardBalance() {
        return cardBalance;
    }

    public void setCardBalance(Integer cardBalance) {
        this.cardBalance = cardBalance;
    }

    public Integer getCardUse() {
        return cardUse;
    }

    public void setCardUse(Integer cardUse) {
        this.cardUse = cardUse;
    }

    public Integer getDiscount() {
        return discount;
    }

    public void setDiscount(Integer discount) {
        this.discount = discount;
    }
}